package com.zycus.automation.exception;

public class UIElementException extends Exception
{

	private static final long serialVersionUID = -8553699246232653450L;

	public UIElementException()
	{
		super();

	}

	public UIElementException(String message, Throwable cause)
	{
		super(message, cause);

	}

	public UIElementException(String message)
	{
		super(message);

	}

	public UIElementException(Throwable cause)
	{
		super(cause);

	}

}
