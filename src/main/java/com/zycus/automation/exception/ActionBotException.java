package com.zycus.automation.exception;

public class ActionBotException extends Exception
{
	private static final long serialVersionUID = 9052780345256903538L;

	public ActionBotException()
	{
		super();

	}

	public ActionBotException(String message, Throwable cause)
	{
		super(message, cause);

	}

	public ActionBotException(String message)
	{
		super(message);

	}

	public ActionBotException(Throwable cause)
	{
		super(cause);

	}

}
